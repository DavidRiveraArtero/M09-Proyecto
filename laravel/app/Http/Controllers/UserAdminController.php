    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required|max:255|unique:users',
            'password' => 'required',
        ]);

        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => $request->password,
            'role_id' => 1
        ]);
        Debugbar::debug("STORE USER");
        if($user){
            Debugbar::debug("DENTRO DE LA CONDICION DE CREAR USER CORRECTAMENTE");
            return redirect()->route('users.index')->with('success','El usuario se añadio correctamente');
        }else{
            Debugbar::debug("DENTRO DE LA CONDICION CON ERROR EN CREAR USER");
            return redirect()->route('users.index')->with('error','Error el usuario no se añadio correctamente');
        }


    }

